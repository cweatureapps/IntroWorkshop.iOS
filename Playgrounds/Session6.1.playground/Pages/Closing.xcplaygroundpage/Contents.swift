# Where to go from here
 
## Developing iOS 9 Apps with Swift (Stanford)
 
 Link: [Developing iOS 9 Apps with Swift](https://itunes.apple.com/au/course/developing-ios-9-apps-swift/id1104579961)
 
 This is the best iOS course out there, and how I originally learned iOS myself.
 Paul Hegarty is an amazing lecturer, and he updates the course every year for the latest version of iOS and Swift.
 
 
## iOS Development in Swift (Plymouth University)
 
 Link: [iOS Development in Swift](https://itunes.apple.com/au/course/ios-development-in-swift/id950659946)
 
 Some decent lectures here, and they are separated into individual topics, many of which are very short and easy to watch when you have a few minutes free.
 
 
## Ray Wenderlich tutorials
 Link: [Ray Wenderlich tutorials](https://www.raywenderlich.com/category/ios)
 
On of the best series of tutorials for iOS, all using hands-on examples to teach concepts. Great when you need to delve into a new part of the framework or learning new topics. Their e-book collections are great too.
 
## AppCoda
 
Link: [AppCoda](http://www.appcoda.com/tutorials/ios/)
 
Lots of great tutorials at AppCoda, you'll often get linked here when searching for tutorial via google.

 
## iOS Frameworks
 
iOS includes many frameworks which provide the interface to other parts of the platform. See [iOS Frameworks](http://developer.apple.com/library/ios/documentation/Miscellaneous/Conceptual/iPhoneOSTechOverview/iPhoneOSFrameworks/iPhoneOSFrameworks.html)

Some of the noteworthy ones:
* AVFoundation and AVKit
* Contacts UI (formerly AddressBook UI)
* EventKit
* HealthKit
* HomeKit
* MapKit
* Message UI
* JavaScriptCore
* WebKit
 
 
## Learn Objective-C
 
If you are serious about going into iOS development, at least for the next couple of years, you'll need experience with Objective-C. Many companies still have a large portion of their legacy codebase in Objective-C , and some are still doing new development in it too. Lots of old tutorials and StackOverflow posts are in Objective-C, and there are many useful libraries that are still written in it.
 
 Here is a nice online game that will teach you Objective-C: [Try Objective-C](http://tryobjectivec.codeschool.com/)



